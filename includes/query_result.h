#ifndef QUERY_RESULT_H
#define QUERY_RESULT_H

#include "../includes/document.h"

/**
* The query_result class is responsible to keep the state of its scores
* as well as the document that the query apears in.
*
*/
class query_result 
{
	friend std::ostream& operator<<(std::ostream& stream, query_result& query);
public:
	/**
	 * @brief constructor for a query result object
	 * 
	 * A query_result object is a key value pair that links a index_item to its scores.
	 * 
	 * @param idx the Index_item
	 * @param scores  the scores
	 */
	query_result(Index_item *idxItem, double score);
	/**
	 * @brief accessor method to return an index_item
	 * 
	 * @return an index items associated with this query.
	 */
	Index_item* getItem() const;
	/**
	 * @brief accessor method to return a score
	 * 
	 * @return double the score associated with this query.
	 */
	double getScore() const;
private:
	Index_item *idxItem;
	double score; 	/** @brief score generated by scoring function */

};

#endif //QUERY_RESULT_H
